package main

import "testing"

func TestComa(t *testing.T) {
	tests := []struct {
		s, want string
	}{
		{"1.0", "1.0"},
		{"12.0", "12.0"},
		{"123.0", "123.0"},
		{"1234.0", "1,234.0"},
		{"12345.0", "12,345.0"},
		{"123456.0", "123,456.0"},
		{"1234567.0", "1,234,567.0"},
		{"12345678.0", "12,345,678.0"},
		{"123456789.0", "123,456,789.0"},
		{"1234567890.0", "1,234,567,890.0"},
		{"1000000000000000000.0", "1,000,000,000,000,000,000.0"},
	}

	for _, test := range tests {
		got := comma(test.s)
		if got != test.want {
			t.Errorf("comma (%q), got %q, want %q", test.s, got, test.want)
		}
	}

}
